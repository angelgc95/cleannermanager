function interpolate(template: string, variables?: Record<string, string | number>) {
  if (!variables) return template;
  return template.replace(/\{\{(\w+)\}\}/g, (_match, key: string) => String(variables[key] ?? ""));
}

function preserveWhitespace(text: string, translated: string) {
  const leading = text.match(/^\s*/)?.[0] ?? "";
  const trailing = text.match(/\s*$/)?.[0] ?? "";
  const trimmedLength = text.trim().length;
  if (trimmedLength === 0) return text;
  return `${leading}${translated}${trailing}`;
}

export function translateText(text: string, language: Language, variables?: Record<string, string | number>) {
  if (!text) return text;

  const core = text.trim();
  if (!core) return text;

  if (language === "en") {
    return preserveWhitespace(text, interpolate(core, variables));
  }

  const translatedExact = SPANISH_TRANSLATIONS[core];
  if (translatedExact) {
    return preserveWhitespace(text, interpolate(translatedExact, variables));
  }

  for (const [matcher, translator] of DYNAMIC_PATTERNS) {
    const match = matcher(core);
    if (match) {
      return preserveWhitespace(text, translator(match, language));
    }
  }

  return preserveWhitespace(text, interpolate(core, variables));
}

export function translateStatus(status: string, language: Language) {
  const normalized = status.toLowerCase().replace(/-/g, "_");
  const english = normalized.replace(/_/g, " ");
  if (language === "en") return english;
  return SPANISH_STATUS_LABELS[normalized] || english;
}

